percent=int(input("Enter Percentage "))
if percent >90 :
    print("A Grade")
elif percent >80 and percent <=90 :
    print("B Grade")
elif percent >70 and percent <=80 :
    print("C Grade")
elif percent >60 and percent <=70 :
    print("D Grade")
elif percent >=50 and percent <=60 :
    print("E Grade")
elif percent >40 and percent <50:
    print("Average")
else :
    print("Fail")